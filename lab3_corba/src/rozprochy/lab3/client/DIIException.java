package rozprochy.lab3.client;

public class DIIException extends Exception {

    public DIIException() {
    }

    public DIIException(String message) {
        super(message);
    }

    public DIIException(Throwable cause) {
        super(cause);
    }

    public DIIException(String message, Throwable cause) {
        super(message, cause);
    }

}
